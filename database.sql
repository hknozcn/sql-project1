CREATE TABLE Departments (
    D_ID INT PRIMARY KEY,
    D_Name VARCHAR(40) NOT NULL UNIQUE,
    Teacher_num INT
);

CREATE TABLE Teachers (
    Teacher_ID INT PRIMARY KEY,
    T_Fname VARCHAR(20) NOT NULL,
    T_Lname VARCHAR(50) NOT NULL,
    Email VARCHAR(30) DEFAULT 'Create new email',
    Phone VARCHAR(13) NOT NULL UNIQUE,
    D_ID INT,
    Tutor BOOLEAN NOT NULL,
    FOREIGN KEY (D_ID) REFERENCES Departments(D_ID)
);
ALTER TABLE Teachers 
ADD CONSTRAINT check_email 
CHECK (POSITION('@' IN Email) > 0 AND Email NOT LIKE '% %');
ALTER TABLE Teachers 
ADD CONSTRAINT chck_phone 
CHECK (Phone ~ '^\+48[4-8]');

CREATE TABLE Subjects (
    S_ID SERIAL PRIMARY KEY,
    S_Name VARCHAR(50) NOT NULL UNIQUE,
    Teacher_ID INT REFERENCES Teachers(Teacher_ID)
);

CREATE TABLE Tutors (
    Tutor_ID SERIAL PRIMARY KEY,
    Teacher_ID INT REFERENCES Teachers(Teacher_ID)
);

CREATE TABLE Classes (
    Class_ID SERIAL PRIMARY KEY,
    Class_Name VARCHAR(50) NOT NULL UNIQUE,
    Class_Level SMALLINT,
    Tutor_ID INT REFERENCES Tutors(Tutor_ID)


CREATE TABLE Rooms (
    Room_ID SERIAL PRIMARY KEY,
    Room_Name VARCHAR(40) NOT NULL DEFAULT 'On Teams',
    Capacity INT NOT NULL DEFAULT 1000
);

CREATE TABLE Tdays (
    Day_ID SERIAL PRIMARY KEY,
    Day_Name VARCHAR(9) NOT NULL,
    CONSTRAINT chck3 CHECK (Day_Name IN ('Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday'))
);

CREATE TABLE Tperiods (
    Period_ID SERIAL PRIMARY KEY,
    Period_Name VARCHAR(15) NOT NULL,
    S_Date DATE,
    E_Date DATE
);

CREATE TABLE TimeTable (
    TimeTable_ID SERIAL PRIMARY KEY,
    Teacher_ID INT REFERENCES Teachers(Teacher_ID),
    S_ID INT REFERENCES Subjects(S_ID),
    Class_ID INT REFERENCES Classes(Class_ID),
    Day_ID INT REFERENCES Tdays(Day_ID),
    Room_ID INT REFERENCES Rooms(Room_ID),
    Period_ID INT REFERENCES Tperiods(Period_ID)
);

